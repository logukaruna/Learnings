public class Loops {
    public static void main(String[] args) {
        //for loop
        for (int i = 5; i < 10; i++) {
            System.out.println("Hi Logu");
        }
        //while
        int n = 6;
        while (n <= 8) {
            System.out.println("Hi machi");
            n++;
        }
        //Do While
        int m = 5;
        do{
            System.out.println("Hello logu");
            m++;
        } while (m <= 10);
    }
}
